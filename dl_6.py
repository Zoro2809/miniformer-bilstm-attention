# Special tokens
# PAD  - padding token to make all sequences same length
# SOS  - start of sentence token added at beginning of target
# EOS  - end of sentence token added at end of target
# UNK  - unknown token for words not in vocabulary

PAD_token = 0
SOS_token = 1
EOS_token = 2
UNK_token = 3

class Vocabulary:
    def __init__(self, name):
        self.name = name
        self.word2index = {
            "<pad>": PAD_token,
            "<s>":   SOS_token,
            "</s>":  EOS_token,
            "<unk>": UNK_token
        }
        self.index2word = {
            PAD_token: "<pad>",
            SOS_token: "<s>",
            EOS_token: "</s>",
            UNK_token: "<unk>"
        }
        self.word_count = {}
        self.n_words = 4  # starts after special tokens

    def add_sentence(self, sentence):
        for word in sentence.split(' '):
            self.add_word(word)

    def add_word(self, word):
        if word not in self.word2index:
            self.word2index[word] = self.n_words
            self.index2word[self.n_words] = word
            self.word_count[word] = 1
            self.n_words += 1
        else:
            self.word_count[word] += 1

